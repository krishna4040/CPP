/******oops*******/
// object oriented programming
// it is an approach to eliminate the pitfalls of procedural oriented programming
// as size of program inc readibility , maintbility , bug free nature , data security dec
// oops try to resolve this
// oops Decomposes the problem in objects and builds data and functions around objects

/*****terminology of oops****/
// classes :: template or datatype for creating objects
// objects :: basic run time entities
// Encapsulation :: wrapping data and functions into single unit
// Abstraction:: ability to decide public private
// inheritence :: properties of one class can be inherited into others
// polymorphism :: ability to take more than one forms
// Dynamic binding :: code which will execute is not known until the program
// message passing :: object.message(info) call format